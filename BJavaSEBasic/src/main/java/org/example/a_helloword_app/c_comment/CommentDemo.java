package org.example.a_helloword_app.c_comment;

/**
 * 3、文档注释
 * 这是一个程序，
 * 是用来讲解注释的使用。
 */
public class CommentDemo {
    /**
     * 这是一个 main 方法，是程序的入口，
     * 必须有这个 main 方法，程序才能执行！
     */
    public static void main(String[] args) {
        // 目标：认识三种注释的写法。
        // 1、单行注释：
        // 打印两行诗句
        System.out.println("我欲乘风归去，又恐琼楼玉宇");
        System.out.println("起舞如飞，转场如戏");

        /*
         * 2、多行注释
         * 打印三行诗句，依次打印
         * */
        System.out.println("生于忧患，死于安乐");
        System.out.println("先天下之忧而忧，后天下之乐而乐");
        System.out.println("起舞弄清影，何似在人间");
    }
}
